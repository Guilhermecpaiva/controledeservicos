    public void logar() {
        String sql = "select * from tbusuarios where login=? and senha=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, TextUsuario.getText());
            pst.setString(2, TextSenha.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                //Obtem o conteudo do campo perfil
                String perfil = rs.getString(6);
                //System.out.println(perfil);
                //Tratamento do perfil de usuario
                if (perfil.equals("admin")) {
                    MenuPrincipal principal = new MenuPrincipal();
                    principal.setVisible(true);
                    MenuPrincipal.MenuRelatorio.setEnabled(true);
                    MenuPrincipal.MenuItemUsuario.setEnabled(true);
                    this.dispose();
                    conexao.close();

                } else {
                    MenuPrincipal principal = new MenuPrincipal();
                    principal.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "usuario ou senha inv√°lido(s)");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

